							<div class="text-justify" style="font-size:13px">
								En virtud del Convenio Marco suscripto entre el COLPROBA, FACA y la SRT, los trabajadores y las trabajadoras que carezcan de letrado de su confianza para ser patrocinados en el ámbito de las Comisiones Médicas Jurisdiccionales, podrán solicitar el sorteo de un/a abogado/a que se haya inscripto previamente en los registros que a tal efecto llevarán los Colegios de Abogados del Departamento o Circunscripción Judicial correspondiente. Las pautas principales de inscripción y funcionamiento del sistema ACOM son: Los Colegios Departamentales habilitarán un registro de abogados y abogadas interesados en patrocinar a los/las trabajadores/ras y/o sus derechohabientes en el ámbito de las Comisiones Médicas Jurisdiccionales. A requerimiento del/la trabajador/ra o sus derechohabientes, mediante el sistema ACOM se procederá al sorteo automático de un/a letrado/a inscripto en el Registro, teniendo en cuenta el Colegio de Abogados que corresponda al domicilio del/la trabajador/ra.</br>
								El/la profesional debe estar disponible en su teléfono declarado, tener matrícula vigente y oficinas de atención en la provincia correspondiente a su Colegio de Abogados para llevar a cabo las obligaciones de este sistema. El/la profesional debe conocer el funcionamiento de las Comisiones Médicas y el de notificaciones electrónicas, Res. 22/18. El la profesional debe aceptar el caso asignado, sin perjuicio de las incompatibilidades previstas en la ley de ejercicio profesional. Debe concertar una entrevista con el/la trabajador/ra o sus derechohabientes dentro de las 72 hs. de asignado el caso. La labor del/la abogado/da es personal e indelegable salvo los supuestos de excepción previstos en las normativas reglamentarias del sistema de CCMM. El/la profesional se obliga a mantener informado al Colegio sobre el avance del trámite, en la medida y oportunidad que este lo disponga. El/la abogado/da debe declarar bajo juramento no haber sido empleado de ninguna ART ni empresa del grupo económico de las mismas durante los 2 (dos) años anteriores a su inscripción. El/la abogado/da debe conocer íntegramente y suscribir el reglamento de adhesión al sistema de registro antes de su inscripción, el que le será oportunamente aportado para su análisis.</br>
							  
								La inscripción en el registro no inhabilita al/la abogado/da para intervenir por su cuenta en el ámbito de las CCMM.								
